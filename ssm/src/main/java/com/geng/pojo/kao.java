package com.geng.pojo;

public class kao {
    private String kaoName;
    private String kaoDate;

    public kao() {
    }

    public kao(String kaoName, String kaoDate) {
        this.kaoName = kaoName;
        this.kaoDate = kaoDate;
    }

    public String getKaoName() {
        return kaoName;
    }

    public void setKaoName(String kaoName) {
        this.kaoName = kaoName;
    }

    public String getKaoDate() {
        return kaoDate;
    }

    public void setKaoDate(String kaoDate) {
        this.kaoDate = kaoDate;
    }
}
