package com.geng.pojo;

public class TotalTwo {
    private String age;
    private String count;

    public TotalTwo() {
    }

    public TotalTwo(String age, String count) {
        this.age = age;
        this.count = count;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getCount() {
        return count;
    }

    public void setCount(String count) {
        this.count = count;
    }
}
